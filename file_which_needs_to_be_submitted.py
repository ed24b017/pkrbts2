    def get_move(self, game_info: GameInfo, current_state: PokerState) -> ActionFold | ActionCall | ActionCheck | ActionRaise | ActionBid:

        # --- Auction ---
        if current_state.street == 'auction':
            equity = emc(current_state.my_hand, current_state.board,
                         current_state.opp_revealed_cards)
            # Bid more when weak (want info), less when strong
            if equity > 0.72:
                frac = 0.04
            elif equity > 0.52:
                frac = 0.07
            else:
                frac = 0.09
            bid = max(1, int(current_state.my_chips * frac))
            return ActionBid(min(bid, current_state.my_chips))

        # --- Look up strategy from CSV ---
        equity = emc(current_state.my_hand, current_state.board,
                     current_state.opp_revealed_cards)
        street_int = STREET_MAP.get(current_state.street, 0)
        bucket = min(int(equity * NUM_BUCKETS), NUM_BUCKETS - 1)

        p = STRATEGY[street_int][bucket].copy()

        # Zero out illegal actions
        legal = set()
        if ActionFold  in current_state.legal_actions: legal.add(0)
        if ActionCall  in current_state.legal_actions: legal.add(1)
        if ActionCheck in current_state.legal_actions: legal.add(2)
        if ActionRaise in current_state.legal_actions:
            legal.update({3, 4, 5})

        for i in range(NUM_ACTIONS):
            if i not in legal:
                p[i] = 0.0

        total = p.sum()
        if total == 0:
            for i in legal:
                p[i] = 1.0 / len(legal)
        else:
            p /= total

        chosen = np.random.choice(NUM_ACTIONS, p=p)

        if chosen == 0:
            return ActionFold()
        elif chosen == 1:
            return ActionCall()
        elif chosen == 2:
            return ActionCheck()
        elif chosen == 3:
            min_r, max_r = current_state.raise_bounds
            return ActionRaise(int(min_r + (max_r - min_r) * 0.2))
        elif chosen == 4:
            min_r, max_r = current_state.raise_bounds
            return ActionRaise(int(min_r + (max_r - min_r) * 0.4))
        elif chosen == 5:
            min_r, max_r = current_state.raise_bounds
            target = min(current_state.my_chips, current_state.opp_chips)
            if target < min_r or target > max_r:
                return ActionCall()
            return ActionRaise(target)

        return ActionCall()



